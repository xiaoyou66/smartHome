package com.example.power;

public class StatusBean {
    /**
     * window : 1
     * right : 2
     * air_statue : 3
     * air_temple : 45
     * water : 6
     */

    private String window;
    private String right;
    private String air_statue;
    private String air_temple;
    private String water;

    public String getWindow() {
        return window;
    }

    public void setWindow(String window) {
        this.window = window;
    }

    public String getRight() {
        return right;
    }

    public void setRight(String right) {
        this.right = right;
    }

    public String getAir_statue() {
        return air_statue;
    }

    public void setAir_statue(String air_statue) {
        this.air_statue = air_statue;
    }

    public String getAir_temple() {
        return air_temple;
    }

    public void setAir_temple(String air_temple) {
        this.air_temple = air_temple;
    }

    public String getWater() {
        return water;
    }

    public void setWater(String water) {
        this.water = water;
    }
}
